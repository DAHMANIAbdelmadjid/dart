class Doctor {
  final int id;
  final String name;
  final String specialty;
  final String imageUrl;
  final double rating;
  final String city;
  final String address;

  Doctor({
    required this.id,
    required this.name,
    required this.specialty,
    required this.imageUrl,
    required this.rating,
    required this.city,
    required this.address,
  });
}
